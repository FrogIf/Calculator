package frog.calculator.express.holder;

public interface MathExpressionLiteral {

    /**
     * addition or positive sign
     */
    String PLUS = "+";

    /**
     * subtraction or negative sign
     */
    String MINUS = "-";

    /**
     * multiplication
     */
    String MULT = "*";

    /**
     * division
     */
    String DIVIDE = "/";

    /**
     * power operation
     */
    String POWER = "^";

    /**
     * percent
     */
    String PERCENT = "%";

    /**
     * factorial
     */
    String FACTORIAL = "!";

    /**
     * imaginary number sign
     */
    String IMAGINARY = "i";

}
